function Euler1=Euler_Propag(ST,Euler0,wBLB0)
%==========================================================================
p=wBLB0(1);
q=wBLB0(2);
r=wBLB0(3);
%==========================================================================
psi=Euler0(1);
theta=Euler0(2);
phi=Euler0(3);
%==========================================================================
phi1=phi+(p+q*sin(phi)*tan(theta)+r*cos(phi)*tan(theta))*ST;
theta1=theta+(q*cos(phi)-r*sin(phi))*ST;
psi1=psi+(q*sin(phi)+r*cos(phi))*sec(theta)*ST;
%==========================================================================
Euler1=[psi1;theta1;phi1];
%==========================================================================
end